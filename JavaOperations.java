public class JavaOperations {
    /*
    Java divides the operators into the following groups:

            Arithmetic operators
            Assignment operators
            Comparison operators
            Logical operators
            Bitwise operators

         Arithmetic Operators

  1, Arithmetic operators are used to perform common mathematical operations

         +, -, *, /, %, ++, --

    2, Java Assignment Operators Assignment operators are used to assign values to variables.

        In the example below, we use the assignment operator (=) to assign the value 10 to a variable called x.  e.g int c = 10

        =, +=, -=, *=, /=, %=, &=, |=, ^=, >>=, <<=

   3, Java Comparison Operators
       Comparison operators are used to compare two values. This is important in programming,
       because it helps us to find answers and make decisions.

       The return value of a comparison is either true or false. These values are known as Boolean values,
       and you will learn more about them in the Booleans and If..Else chapter.

       ==, !=, >, <, >=, <=

  4, Java Logical Operators
        You can also test for true or false values with logical operators.

        Logical operators are used to determine the logic between variables or values

        && , ||, !

    */
}
